package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3);//3 this will leave the remainder out
        System.out.println(number%3);//1 modulus will include the remainder.
        System.out.println(30%7);// 2 is the remainder.
        System.out.println(90%12);//6 is the remainder.
        System.out.println(678%2);//even
        System.out.println(234567808%2);//even


    }
}
