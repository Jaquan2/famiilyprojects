package class6;
/*
Increment Operators
 */
public class IncrementOperator {
    public static void main(String[] args) {

        int number1=10;
        number1=number1+1;
        System.out.println(number1);

        number1+=1;
        System.out.println(number1);

        number1++;
        System.out.println(number1);

        int number2=10;
        number2--;// decrease
        System.out.println(number2);
    }
}
