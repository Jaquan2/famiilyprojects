package class2;

public class DecimalNumbers {
    public static void main(String[] args) {
        /*
        If you want to store decimal number (10.2,14.5) into computers memory using
        Java we can use following types of boxes.
        float - for small decimal numbers
        decimal
         */
        float myNumberBox=10.5f;
        System.out.println(myNumberBox);
        double largeDeciamlBox=134545455.12121;
        System.out.println(largeDeciamlBox);
        /*
        If I want to store characters (#,$,o,s,4) we can use char datatype
         */
        char letter='s';
        char gender='M';
        char symbol='#';
        char numberChar='1';
       // S in String belong to different datatypes
        String name="Jay";

        boolean hungry=true; // true means yes
        boolean sleepy=false; // false means no

        System.out.println();
    }
}
