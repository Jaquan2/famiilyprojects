package class8;

public class NestedLoops5 {
    public static void main(String[] args) {
        for (int j=0; j<=5; j++){
            System.out.print(j+" ");
        }
        System.out.println();
        for (int j=0; j<=5; j++){
            System.out.print(j*2+" ");
        }
        System.out.println();
        for (int j=0; j<=5; j++){
            System.out.print(j*3+" ");
        }
        System.out.println();





    }
}